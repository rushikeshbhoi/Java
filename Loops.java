class Loops 
{
	public static void main(String[] args) 
	{
		// while loop
		int n = 1;
			System.out.println("While Loop");

		while(n<=5){
			System.out.println(n);
			n++;
		}

		System.out.println("Do While Loop");

		// do-while loop
		int m = 1;
		do{
			System.out.println(m);
			m++;
	}
	while(m<=5);

		System.out.println("For Loop");

		// for loop
		for(int i=5; i>=1; i--){
			System.out.println(i);
		}

}
}
