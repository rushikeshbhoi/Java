class Solution 
{
	public static int add(int a, int b, int c) 
	{
		return a+b+c;
	}

	public static int sub(double a, double b){
		return (int) (a-b);
	}

	public static void mul(int a, int b){
		int c = a*b;
		System.out.println(c);
	}

	public static double div(int a, int b){
		return a/b;
	}
}
